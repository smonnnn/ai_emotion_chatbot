# Emotion-Adaptive Chatbot

A simple chatbot that detects user emotions and generates empathetic responses.

## Setup

1. If needed, modify setup.sh to install the right pytorch version for your machine.
2. Run the setup script with `bash ./setup.sh`.
3. Set your mistral ai API key in the generated .env file.
4. Enable the generated venv with source. /.venv/bin/activate.
5. Run `python main.py`.